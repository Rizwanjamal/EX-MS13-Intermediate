// == just checks the values on both sides

'rizwan' == 'rizwan' // true
'2' == 2             // true

// === checks both the values & data types on both sides

'2' === 2        // false

// != checks if the values on both sides are not equal

5 != 8           // true
5 != '5'         // false
5 != 5           // false

// != checks if the values & data types on both sides are not equal

5 !== 8           // true
5 !== '5'         // true
5 !== 5           // false

// </> checks if one value is less than (<) or greater than(>) the other one

8 > 7             // true
8 < 10            // true
8 > 10            // false

// <=/>= checks if one value is less than or equal (<=) or greater than or equal(>=) to the other one

8 >= 8            // true
8 <= 10           // true
8 >= 10           // false








